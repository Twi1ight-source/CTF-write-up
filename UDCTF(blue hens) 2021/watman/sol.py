import string

def encode(inp2):
	v10=ord('D')
	v8=120
	v16= v10 &1
	tmp=inp2[0]
	v10=tmp
	if v16==0:
		v4=1
	else:
		v4=-1
	v8+=v4
	v13=v8*tmp 
	return (v13%257) & 0xff

